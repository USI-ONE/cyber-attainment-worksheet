/**
 * Per-control assessment questionnaires for NIST CSF 2.0.
 *
 * Each entry is three questions tailored to the specific control's outcome,
 * plus a short improvement prompt for the optional tier-5 unlock. Questions
 * are written to leave no ambiguity about what would constitute a "Yes":
 * they reference specific named artifacts (a register, a written document,
 * meeting minutes), specific scopes (all in-scope systems, every Tier-1
 * supplier, every privileged account), and specific cadences (in the past
 * 12 months, at least quarterly, etc.). Hints describe what the assessor
 * should look for as evidence.
 *
 * Q1 always asks whether the control's primary artifact exists. Q2 asks
 * whether it's actually applied at the scope the outcome demands. Q3 asks
 * whether it's been reviewed, audited, or measured with a documented
 * outcome — that's the move from "documented" to "managed" on the CMM
 * ladder.
 *
 * AssessmentWizard.tsx looks up the entry by control_id; if a control is
 * missing here it falls back to the generic CMM ladder questions defined
 * in the same component.
 */

